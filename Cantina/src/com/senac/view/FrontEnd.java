    // => quando for inserir cliente
    public void cadastrarCliente() {
        ClientesGerenciar cc = new ClientesGerenciar();
        desktop.removeAll();
        cc.setVisible(true);
        desktop.add(cc);
        SwingUtilities.updateComponentTreeUI(desktop);
    }

    // => quando for inserir funcionario
    public void cadastrarFuncionario() {
        FuncionariosGerenciar fc = new FuncionariosGerenciar();
        desktop.removeAll();
        fc.setVisible(true);
        desktop.add(fc);
        SwingUtilities.updateComponentTreeUI(desktop);
    }

