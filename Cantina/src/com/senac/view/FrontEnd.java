    private void menuVDClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuVDClienteActionPerformed
        ReportXLS rp = new ReportXLS();
        int matricula = Integer.parseInt(JOptionPane.showInputDialog("Matricula do cliente:"));
        try {
            rp.reportUsuario(matricula);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar dados", "", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_menuVDClienteActionPerformed

